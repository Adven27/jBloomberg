    private static final Map<String, RealtimeField> fields = new HashMap<> ();
    static {
      for (RealtimeField f : values()) fields.put(f.fieldCode, f);
    }
    private final String fieldCode;

    RealtimeField() {
      this.fieldCode = name();
    }

    RealtimeField(String fieldCode) {
      this.fieldCode = fieldCode;
    }

    /**
     *
     * @param fieldName the field to check
     * @return true if fieldName is a valid enum name
     *
     * @throws NullPointerException if fieldName is null
     */
    public static boolean contains(String fieldName) {
        return fields.containsKey(requireNonNull(fieldName));
    }

    /**
     *
     * @param fieldName the field to check
     * @return true if fieldName is a valid enum name, ignoring case
     *
     * @throws NullPointerException if fieldName is null
     */
    public static boolean containsIgnoreCase(String fieldName) {
        return fields.containsKey(fieldName.toUpperCase());
    }

    /**
     * Returns the enum constant of this type with the specified name. The string must match an identifier used to
     * declare an enum constant in this type (ignoring the case).
     *
     * @return the enum constant with the specified name
     *
     * @throws IllegalArgumentException if this enum type has no constant with the specified name
     * @throws NullPointerException if fieldName is null
     */
    public static RealtimeField valueOfIgnoreCase(String fieldName) {
        return fields.get(fieldName.toUpperCase());
    }
